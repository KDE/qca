    QCOMPARE(QCA::BigInteger("255").toString(), QStringLiteral("255"));
    QCOMPARE(QCA::BigInteger("-255").toString(), QStringLiteral("-255"));
    QCOMPARE(QCA::BigInteger("255"), QCA::BigInteger(QCA::BigInteger(255).toArray()));
